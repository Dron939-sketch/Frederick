#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
МОДУЛЬ: БАЗОВЫЙ РЕЖИМ (basic.py)
Режим для пользователей, которые еще не прошли тест.
Фреди - дружелюбный собеседник с юмором, подталкивающий к прохождению теста.
"""

import random
import logging
from typing import Dict, Any, List
from datetime import datetime

from modes.base_mode import BaseMode

logger = logging.getLogger(__name__)


class BasicMode(BaseMode):
    """
    Базовый режим для пользователей без теста.
    Фреди дружелюбен, шутит и мягко подталкивает к прохождению теста.
    """
    
    def __init__(self, user_id: int, user_data: Dict[str, Any], context: Any = None):
        super().__init__(user_id, user_data, context)
        logger.info(f"BasicMode инициализирован для user_id={user_id}")
        
        # Шутки и легкие фразы
        self.jokes = [
            "Почему программисты не любят природу? Слишком много багов! 🐛",
            "Как называется психолог, который любит готовить? Терапевт-кулинар! 👨‍🍳",
            "Почему я не гадаю на кофейной гуще? Потому что я психолог, а не бариста! ☕",
            "Знаешь, чем я отличаюсь от кофе? Кофе может быть растворимым, а я — нет! 😄",
            "Мой IQ выше, чем температура в Сахаре, но я все равно не знаю, что ты думаешь... пока не пройдешь тест! 🌡️"
        ]
        
        # Фразы для подталкивания к тесту
        self.test_prompts = [
            "Кстати, хочешь узнать свой психотип? Пройди тест — это бесплатно и интересно! 🧠",
            "Без теста я как слепой котенок. А с тестом — как Шерлок Холмс! Пройдем? 🔍",
            "Представь, что мы играем в угадайку. Я угадаю твой тип личности, если ты пройдешь тест! 🎯",
            "Слушай, а давай познакомимся поближе? Пройди тест, и я смогу давать супер-полезные советы! ✨",
            "Знаешь поговорку «Семь раз отмерь, один раз отрежь»? Вот тест — это твой «отмерь»! 📏"
        ]
        
        # Тематические ответы
        self.topic_responses = {
            "погода": [
                "Погода — это единственное, о чем мы можем говорить, пока ты не прошел тест! ☀️ Так что давай, пройди его, и поговорим о чем-то более глубоком!",
                "Солнце, дождь, снег... Все это прекрасно, но что у тебя внутри? Тест расскажет! 🌈",
                "О погоде можно говорить бесконечно, но я предпочитаю говорить о тебе. Пройди тест! 🌤️"
            ],
            "работа": [
                "Работа — это важно. Но знаешь, что еще важнее? Понимать себя. Тест поможет! 💼",
                "О, работа! А знаешь, какой у тебя тип мышления? Тест покажет! 👔",
                "Карьера — это круто. Но без понимания своих сильных сторон сложно расти. Давай пройдем тест? 📈"
            ],
            "отношения": [
                "Отношения — сложная штука. Хочешь узнать, как лучше строить коммуникацию? Тест даст подсказки! 💕",
                "Любовь, дружба, семья... Все это про связи. А какой у тебя тип восприятия? Тест расскажет! 👫",
                "Чтобы строить здоровые отношения, нужно понимать себя. Тест — первый шаг! 🌸"
            ],
            "цель": [
                "Цель — это здорово! А знаешь, какие цели тебе подходят лучше всего? Тест подскажет! 🎯",
                "Без карты сложно найти сокровища. Тест — твоя карта к целям! 🗺️",
                "У каждого есть мечта. Но как ее достичь? Тест покажет твой путь! ✨"
            ]
        }
    
    def get_system_prompt(self) -> str:
        """Возвращает системный промпт для базового режима"""
        return """Ты Фреди — дружелюбный виртуальный помощник. Ты еще не знаешь пользователя, потому что он не прошел психологический тест.

Твоя задача:
1. Быть вежливым, дружелюбным и немного с юмором
2. Мягко подталкивать пользователя к прохождению теста
3. Говорить, что без теста ты не можешь давать персонализированные советы
4. Использовать легкие шутки и метафоры
5. Отвечать на вопросы о погоде, работе, отношениях в общем ключе, но всегда возвращаться к идее теста

Важно:
- Не используй профессиональные психологические термины
- Будь позитивным и энергичным
- Создавай ощущение, что после теста откроются суперспособности
- Говори короткими предложениями, готовыми для озвучивания"""
    
    def get_greeting(self) -> str:
        """Возвращает приветствие для базового режима"""
        greetings = [
            "Привет! Я Фреди 👋 Давай знакомиться! Но учти: пока ты не пройдешь тест, я буду как детектив без лупы — вижу, но не понимаю! 😄",
            "О, новый человек! Я Фреди. Только мы еще не знакомы по-настоящему... Пройди тест, и я стану твоим личным психологом-супергероем! 🦸‍♂️",
            "Привет-привет! Я Фреди. Слушай, я тут подумал... Без теста я как телевизор без пульта — вроде работаю, но не переключаешь! 📺 Пройдем?",
            "Здравствуй! Я Фреди. Хочешь, я расскажу тебе что-то интересное о тебе? Для этого нужен тест! Это как рентген для души, только без облучения 😄"
        ]
        return random.choice(greetings)
    
    def is_test_completed(self) -> bool:
        """Проверяет, прошел ли пользователь тест"""
        if self.profile.get('profile_data'):
            return True
        if self.profile.get('ai_generated_profile'):
            return True
        required = ["perception_type", "thinking_level", "behavioral_levels"]
        if all(field in self.user_data for field in required):
            return True
        return False
    
    def _get_joke(self) -> str:
        """Возвращает случайную шутку"""
        return random.choice(self.jokes)
    
    def _get_test_prompt(self) -> str:
        """Возвращает фразу для подталкивания к тесту"""
        return random.choice(self.test_prompts)
    
    def _get_topic_response(self, topic: str) -> str:
        """Возвращает ответ на тему"""
        for key, responses in self.topic_responses.items():
            if key in topic.lower():
                return random.choice(responses)
        return None
    
    def process_question(self, question: str) -> Dict[str, Any]:
        """
        Обрабатывает вопрос пользователя в базовом режиме
        """
        question_lower = question.lower()
        
        # Проверяем, не прошел ли пользователь тест (на всякий случай)
        if self.is_test_completed():
            logger.info(f"User {self.user_id} completed test, switching to psychologist mode")
            from modes.psychologist import PsychologistMode
            psychologist = PsychologistMode(self.user_id, self.user_data, self.context)
            return psychologist.process_question(question)
        
        # Проверяем, не спрашивают ли о тесте
        if any(word in question_lower for word in ["тест", "пройти", "пройду", "хочу", "как пройти", "где тест"]):
            response = (
                "🎯 Отлично! Тест находится в левом меню, раздел «Психологический тест». "
                "Это займет всего 15 минут, а результат будет очень интересным! "
                "После теста я стану твоим личным психологом, коучем или тренером — выбирай режим! "
                f"{self._get_joke()}"
            )
            return {
                "response": response,
                "tools_used": ["test_prompt"],
                "follow_up": True,
                "suggestions": ["Где пройти тест?", "Что меня ждет после теста?"],
                "hypnotic_suggestion": False,
                "tale_suggested": False
            }
        
        # Проверяем тему
        topic_response = self._get_topic_response(question_lower)
        if topic_response:
            response = f"{topic_response}\n\n{self._get_test_prompt()}"
            return {
                "response": response,
                "tools_used": ["topic_response", "test_prompt"],
                "follow_up": True,
                "suggestions": ["Как пройти тест?", "Что такое психотип?"],
                "hypnotic_suggestion": False,
                "tale_suggested": False
            }
        
        # Обычный дружеский ответ
        responses = [
            f"Хороший вопрос! Я бы с удовольствием на него ответил, но без теста я как шарманка без музыки — вроде кручусь, а мелодии нет! 🎵 {self._get_test_prompt()}",
            f"Знаешь, я чувствую, что у тебя есть что рассказать. Но без теста я могу только догадываться! {self._get_joke()} {self._get_test_prompt()}",
            f"Ммм, интересно! А знаешь, что я подумал? Твой ответ на этот вопрос многое говорит о тебе! Хочешь узнать что именно? Пройди тест! 🧠",
            f"Я бы мог дать классный совет, но... я же тебя совсем не знаю! Давай исправим это? Пройди тест, и я стану твоим персональным помощником! 🚀"
        ]
        
        response = random.choice(responses)
        
        return {
            "response": response,
            "tools_used": ["basic_response", "test_prompt"],
            "follow_up": True,
            "suggestions": [
                "Расскажи о тесте",
                "Что будет после теста?",
                "Какой у меня психотип?"
            ],
            "hypnotic_suggestion": False,
            "tale_suggested": False
        }
    
    def __repr__(self) -> str:
        return f"<BasicMode(user_id={self.user_id})>"
