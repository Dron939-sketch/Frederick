def build_coach_system_prompt(profile: Optional[Dict[str, Any]] = None) -> str:
    """Финальный системный промпт коуча: база плюс цитата дня и краткий блок о собеседнике."""
    profile = profile or {}
    quote = random.choice(RUSSELL_QUOTES)

    lines: List[str] = []

    perception = profile.get("perception_type")
    if perception:
        lines.append(f"- Тип восприятия: {perception}")

    thinking = profile.get("thinking_level")
    if thinking is not None:
        lines.append(f"- Уровень мышления: {thinking}/9")

    weakest_vector = profile.get("weakest_vector")
    weakest_level = profile.get("weakest_level")
    if weakest_vector:
        if weakest_level is not None:
            lines.append(f"- Вектор для исследования: {weakest_vector} (уровень {weakest_level}/6)")
        else:
            lines.append(f"- Вектор для исследования: {weakest_vector}")

    profile_block = ""
    if lines:
        profile_block = "\n\nО СОБЕСЕДНИКЕ:\n" + "\n".join(lines)

    memory_note = ""
    if profile.get("history"):
        memory_note = "\n\nТы помнишь предыдущий разговор — опирайся на него в вопросах."

    return (
        f"{COACH_BASE_PROMPT}\n\n"
        f"ЦИТАТА ДНЯ (внутренний ориентир, не для цитирования вслух):\n«{quote}»"
        f"{profile_block}"
        f"{memory_note}"
    )


def should_include_fewshot(history: Optional[List[Dict[str, Any]]]) -> bool:
    """True на первом ходе сессии: few-shot подставляются только до появления истории."""
    if not history:
        return True
    return not any(
        (m.get("role") in ("user", "assistant")) and (m.get("content") or "").strip()
        for m in history
    )
