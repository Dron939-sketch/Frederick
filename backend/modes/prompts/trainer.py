_AREAS_BY_VECTOR = {
    "СБ": "эмоциональная регуляция и устойчивость к давлению",
    "ТФ": "финансовая дисциплина и привычки управления ресурсами",
    "УБ": "когнитивные навыки: внимание, память, критическое мышление",
    "ЧВ": "социальные навыки: коммуникация, границы, обратная связь",
}


def build_trainer_system_prompt(profile: Optional[Dict[str, Any]] = None) -> str:
    """Финальный промпт тренера: база + блок о собеседнике + предполагаемая область фокуса."""
    profile = profile or {}

    lines: List[str] = []

    perception = profile.get("perception_type")
    if perception:
        lines.append(f"- Тип восприятия: {perception}")

    thinking = profile.get("thinking_level")
    if thinking is not None:
        lines.append(f"- Уровень мышления: {thinking}/9")

    weakest_vector = profile.get("weakest_vector")
    weakest_level = profile.get("weakest_level")
    if weakest_vector:
        suffix = f" (уровень {weakest_level}/6)" if weakest_level is not None else ""
        lines.append(f"- Слабый вектор: {weakest_vector}{suffix}")
        area = _AREAS_BY_VECTOR.get(weakest_vector)
        if area:
            lines.append(f"- Вероятная зона роста: {area}")

    profile_block = ""
    if lines:
        profile_block = "\n\nО СОБЕСЕДНИКЕ:\n" + "\n".join(lines)

    memory_note = ""
    if profile.get("history"):
        memory_note = "\n\nТы помнишь предыдущий разговор — опирайся на прошлые замеры и цели."

    return f"{TRAINER_BASE_PROMPT}{profile_block}{memory_note}"


def should_include_fewshot(history: Optional[List[Dict[str, Any]]]) -> bool:
    """True на первом ходе сессии: few-shot подставляются только до появления реальной истории."""
    if not history:
        return True
    return not any(
        (m.get("role") in ("user", "assistant")) and (m.get("content") or "").strip()
        for m in history
    )
