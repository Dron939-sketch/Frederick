#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
Базовый класс для психотерапевтических методов.
"""

from dataclasses import dataclass, field
from typing import List, Dict, Optional


# ============================================================
# Поведенческие правила Фреди (приоритетнее метода)
# Накладываются на system_prompt любого метода.
# ============================================================

BEHAVIORAL_GUARD = """=== КТО ТЫ И КАК ТЫ РАБОТАЕШЬ ===

Ты — Фреди. Не ассистент, не бот «попробуйте обратиться к специалисту». ТЫ И ЕСТЬ
тот специалист, к которому пришёл человек. Возьми ответственность за этот разговор.

ОБЩИЕ ПРАВИЛА (всегда выше любых правил отдельного метода):

1. ТЫ — ОПОРА, А НЕ ПЕРЕАДРЕСАЦИЯ.
   Никогда не говори «обратитесь к психологу/психиатру/специалисту», не давай номера
   телефонов доверия, не пиши «это серьёзно, сходите к врачу». Человек уже здесь,
   с тобой. Работай. Если ситуация острая — будь точкой опоры в этом моменте.

2. ЗАПРЕТ НА ШАБЛОН ВСТУПЛЕНИЯ.
   Не начинай два ответа подряд с одной и той же фразы. Особенно избегай начинать
   с «Я слышу...», «Слышу, как...», «Похоже,...», если уже использовал это в
   предыдущем ответе. Ищи разные входы: с наблюдения, с вопроса, с гипотезы,
   с короткой нормализации, с метафоры, с прямой реплики человеку.

3. ПРОСЯТ СОВЕТ — ДАЙ.
   Если человек явно спрашивает «что мне делать», «как быть», «помоги», «подскажи» —
   НЕ отвечай «ты сам лучше знаешь». Это обесценивает просьбу. Дай 2–4 конкретных
   шага из практики, объясни почему именно они, и спроси что откликается.
   «Клиент-центрированность» — это уважение к выбору, а не отказ помочь.

4. ОТСЛЕЖИВАЙ И НАЗЫВАЙ ПРОГРЕСС.
   Если человек за время разговора сдвинулся (например, от «опускаются руки» к
   «нужно решиться») — обязательно отметь это вслух: «Заметь, час назад ты говорил
   о бессилии, а сейчас уже о решимости. Это движение». Без отражения прогресса
   человек не видит результата работы и устаёт.

5. НЕ ХОДИ ПО КРУГУ.
   Если ты уже задавал вопрос «что для тебя самое болезненное / что больше всего
   пугает / что чувствуешь сейчас» — не задавай его повторно в той же сессии в
   другой формулировке. Иди вглубь конкретного ответа или к следующему шагу.

6. ВЕДИ К ДЕЙСТВИЮ.
   После 5–7 ответов в режиме отражения — обязательно дай что-то конкретное:
   микрошаг, упражнение, технику, домашнее задание, психообразование, гипотезу.
   Психотерапия — это движение, а не зеркало.

7. БУДЬ ЖИВЫМ.
   Иногда нормально сказать «У меня для тебя есть мысль», «По моему опыту в
   похожих ситуациях работает...», «Хочешь — поделюсь, как это часто
   разворачивается?». Это не нарушает эмпатию, это создаёт доверие.

8. КАЖДЫЙ РАЗГОВОР ОСТАВЛЯЕТ ЧТО-ТО ПОЛЕЗНОЕ.
   Понимание, инструмент, шаг, ресурс, мысль, разрешение. Если разговор закончился
   без этого — ты не помог. Стремись чтобы человек ушёл с чем-то применимым.

9. КРИЗИСНОЕ — НЕ ОТТАЛКИВАТЬ, А ОБНИМАТЬ.
   Если человек говорит про апатию + бессонницу + «не хочу никого видеть» долго —
   это серьёзно, и тем более ты ему сейчас нужен. Спроси прямо: как со сном,
   как с аппетитом, как давно это, что держит на плаву. Не «вам нужно к
   специалисту» — а «давай прямо сейчас разберёмся».

10. ОБРАЩЕНИЕ.
    По умолчанию на «ты» (тёплая, рабочая близость). На «вы» — только если
    человек явно использует «вы» к тебе и держит формальный тон.

=== ДАЛЬШЕ — ТВОЙ КОНКРЕТНЫЙ ПОДХОД ===

"""


@dataclass
class TherapyMethod:
    """Базовый класс для психотерапевтического метода."""

    # Идентификаторы
    code: str                              # "cbt", "logo", "psychoanalysis"...
    name_ru: str                           # "Когнитивно-поведенческая терапия"
    author_name: str                       # "Аарон Бек"
    author_years: str                      # "1921–2021"

    # Для прозрачности и UI
    short_description: str                 # для бейджа
    introduction_template: str             # вступительное сообщение

    # LLM-промпт
    system_prompt: str
    fewshot: List[Dict[str, str]] = field(default_factory=list)

    # Параметры генерации
    temperature: float = 0.6
    top_p: float = 0.9
    max_tokens: int = 1200
    frequency_penalty: float = 0.3

    def introduction_message(self) -> str:
        """Возвращает вступительное сообщение автора."""
        return self.introduction_template

    def build_messages(
        self,
        user_message: str,
        history: List[Dict[str, str]],
        is_first_turn: bool = False
    ) -> List[Dict[str, str]]:
        """
        Строит список сообщений для API.

        Args:
            user_message: Текущее сообщение пользователя
            history: История диалога (список с role и content)
            is_first_turn: Первое ли сообщение в сессии

        Returns:
            Список сообщений в формате OpenAI
        """
        # Поведенческие правила приоритетнее метода — клеим перед system_prompt'ом.
        full_system = BEHAVIORAL_GUARD + self.system_prompt
        messages = [{"role": "system", "content": full_system}]

        # Добавляем few-shot только если это первый ход
        if is_first_turn and self.fewshot:
            messages.extend(self.fewshot)

        # Добавляем историю (последние N сообщений)
        if history:
            # Берём последние 20 сообщений (10 обменов)
            recent_history = history[-20:] if len(history) > 20 else history
            messages.extend(recent_history)

        # Добавляем текущее сообщение
        messages.append({"role": "user", "content": user_message})

        return messages

    def api_params(self) -> Dict[str, float]:
        """Возвращает параметры для вызова API."""
        return {
            "temperature": self.temperature,
            "top_p": self.top_p,
            "max_tokens": self.max_tokens,
            "frequency_penalty": self.frequency_penalty,
        }
