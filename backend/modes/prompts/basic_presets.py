def get_preset_text(key: str) -> str:
    """Текст промпта по ключу. Неизвестный ключ → fallback на 'current'."""
    return _PRESETS.get((key or "").strip().lower(), _PRESETS[PRESET_KEY_DEFAULT])


def list_presets() -> Dict[str, Dict]:
    """Возвращает {key: meta-dict} для админ-UI (без полного текста промпта)."""
    return {item["key"]: item for item in PRESET_META}


def all_keys() -> list:
    return [item["key"] for item in PRESET_META]
