    def get_tale_for_issue(self, issue: str) -> Optional[Dict[str, Any]]:
        """
        Возвращает сказку по запросу
        
        Args:
            issue: проблема (страх, отношения, деньги и т.д.)
            
        Returns:
            словарь со сказкой или None
        """
        issue_lower = issue.lower()
        
        # Ищем по точному совпадению
        if issue_lower in self.tales:
            return self.tales[issue_lower]
        
        # Ищем по карте соответствия
        tale_keys = self.issue_mapping.get(issue_lower, ['growth_tree'])
        if tale_keys:
            tale_key = random.choice(tale_keys)
            return self.tales.get(tale_key)
        
        # Случайная сказка, если ничего не подошло
        random_key = random.choice(list(self.tales.keys()))
        return self.tales[random_key]
    
    def get_tale_by_id(self, tale_id: str) -> Optional[Dict[str, Any]]:
        """
        Возвращает сказку по ID
        
        Args:
            tale_id: идентификатор сказки
            
        Returns:
            словарь со сказкой или None
        """
        return self.tales.get(tale_id)
    
    def get_random_tale(self) -> Dict[str, Any]:
        """
        Возвращает случайную сказку
        
        Returns:
            словарь со случайной сказкой
        """
        random_key = random.choice(list(self.tales.keys()))
        return self.tales[random_key]
    
    def get_all_tale_ids(self) -> List[str]:
        """
        Возвращает список всех ID сказок
        
        Returns:
            список идентификаторов
        """
        return list(self.tales.keys())
    
    def get_metaphor(self, issue: str) -> str:
        """
        Возвращает короткую метафору по проблеме
        
        Args:
            issue: проблема
            
        Returns:
            метафора
        """
        issue_lower = issue.lower()
        metaphors = self.metaphors.get(issue_lower, [])
        
        if metaphors:
            return random.choice(metaphors)
        
        # Общая метафора
        general_metaphors = [
            "Жизнь — это река. Она течёт, и ты плывёшь. Иногда спокойно, иногда бурно. Но ты всегда на воде.",
            "Ты — как дерево. У тебя есть корни (то, что держит), ствол (то, что есть), ветви (то, куда тянешься).",
            "Каждый день — это страница. Иногда она пишется легко, иногда трудно. Но книга всё равно пишется."
        ]
        return random.choice(general_metaphors)
    
    def get_tale_text(self, tale_id: str) -> str:
        """
        Возвращает только текст сказки
        
        Args:
            tale_id: идентификатор сказки
            
        Returns:
            текст сказки
        """
        tale = self.get_tale_by_id(tale_id)
        if tale:
            return tale.get('text', '')
        return "Сказка не найдена"
    
    def get_tale_info(self, tale_id: str) -> Dict[str, Any]:
        """
        Возвращает информацию о сказке без полного текста
        
        Args:
            tale_id: идентификатор сказки
            
        Returns:
            информация о сказке
        """
        tale = self.get_tale_by_id(tale_id)
        if tale:
            return {
                'title': tale.get('title', ''),
                'issue': tale.get('issue', ''),
                'moral': tale.get('moral', ''),
                'duration': tale.get('duration', ''),
                'available': True
            }
        return {'available': False, 'error': 'Сказка не найдена'}


