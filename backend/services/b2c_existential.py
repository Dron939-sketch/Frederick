ALL_EXISTENTIAL_CODES: List[str] = list(EXISTENTIAL_STANCES.keys())
ALL_LOCUS_CODES: List[str] = list(LOCUS_OF_CONTROL.keys())


def get_existential(code: Optional[str]) -> Optional[Dict[str, Any]]:
    if not code:
        return None
    c = code.strip().lower()
    if c in ("none", "", "null"):
        return None
    return EXISTENTIAL_STANCES.get(c)


def get_locus(code: Optional[str]) -> Optional[Dict[str, Any]]:
    if not code:
        return None
    c = code.strip().lower()
    if c in ("none", "", "null"):
        return None
    return LOCUS_OF_CONTROL.get(c)


