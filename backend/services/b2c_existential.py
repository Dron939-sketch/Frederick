def axes_summary(
    existential_stance: Optional[str],
    locus_of_control: Optional[str],
) -> Dict[str, Any]:
    """Возвращает разрешённые карточки + текстовую сводку «осей»."""
    es = get_existential(existential_stance)
    lc = get_locus(locus_of_control)
    return {
        "existential": es,
        "locus": lc,
        "compass": (
            f"{es['name_ru'] if es else '—'}  ×  {lc['name_ru'] if lc else '—'}"
        ),
    }
