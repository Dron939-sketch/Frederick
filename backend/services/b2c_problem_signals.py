ALL_PROBLEM_CODES: List[str] = list(PROBLEMS.keys())


def get_problem(code: Optional[str]) -> Optional[Dict[str, Any]]:
    """Получает запись проблемы по коду. None если нет/none/empty."""
    if not code:
        return None
    c = code.strip().lower()
    if c in ("none", "", "null"):
        return None
    return PROBLEMS.get(c)


def get_tool(tool_code: str) -> Optional[Dict[str, Any]]:
    """Получает UI-описание инструмента по коду."""
    if not tool_code:
        return None
    return TOOLS_UI.get(tool_code.strip().lower())


def render_second_touch(problem_code: str, name: str, voice: bool = False) -> str:
    """Рендерит ВТОРОЕ касание с подстановкой имени."""
    p = get_problem(problem_code)
    if not p:
        return ""
    tpl = p["second_touch_voice"] if voice else p["second_touch"]
    n = (name or "").strip()
    if not n:
        return tpl.replace("{name}, ", "").replace("{name}", "").strip()
    return tpl.replace("{name}", n)


