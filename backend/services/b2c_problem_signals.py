def filter_actionable(
    problem_signals: List[Dict[str, Any]],
) -> List[Dict[str, Any]]:
    """Возвращает только сигналы с weight >= weight_floor проблемы.

    Используется на этапе рекомендации, чтобы не предлагать инструмент
    по слабому/неуверенному сигналу.
    """
    actionable: List[Dict[str, Any]] = []
    for sig in problem_signals or []:
        code = (sig.get("code") or "").strip().lower()
        weight = float(sig.get("weight") or 0.0)
        p = get_problem(code)
        if not p:
            continue
        if weight >= float(p.get("weight_floor", 0.4)):
            actionable.append({
                "code": code,
                "weight": weight,
                "evidence": sig.get("evidence", ""),
                "tool_code": p["tool_code"],
                "tool": get_tool(p["tool_code"]),
                "name_ru": p["name_ru"],
                "best_send_time_msk": p.get("best_send_time_msk", ""),
            })
    actionable.sort(key=lambda x: x["weight"], reverse=True)
    return actionable[:3]


def top_recommendation(
    problem_signals: List[Dict[str, Any]],
) -> Optional[Dict[str, Any]]:
    """Возвращает топ-1 рекомендацию (или None если ничего actionable)."""
    a = filter_actionable(problem_signals)
    return a[0] if a else None
