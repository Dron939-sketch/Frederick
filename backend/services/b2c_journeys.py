ALL_JOURNEY_CODES: List[str] = list(JOURNEYS.keys())


def get_journey(code: Optional[str]) -> Optional[Dict[str, Any]]:
    """Получает journey по коду. None если none/empty."""
    if not code:
        return None
    c = code.strip().lower()
    if c in ("none", "", "null"):
        return None
    return JOURNEYS.get(c)


def render_journey_hook(
    journey_code: str,
    name: str,
    voice: bool = False,
) -> str:
    """Рендерит narrative hook А→Б→С с подстановкой имени."""
    j = get_journey(journey_code)
    if not j:
        return ""
    tpl = j["narrative_hook_voice"] if voice else j["narrative_hook"]
    n = (name or "").strip()
    if not n:
        return tpl.replace("{name}, ", "").replace("{name}", "").strip()
    return tpl.replace("{name}", n)


def get_tool_chain(journey_code: str) -> List[Dict[str, Any]]:
    """Возвращает цепочку инструментов с подгруженным UI-описанием."""
    j = get_journey(journey_code)
    if not j:
        return []
    try:
        from services.b2c_problem_signals import get_tool
    except Exception:
        get_tool = lambda x: None  # noqa: E731
    out = []
    for step in j.get("tool_chain", []):
        tool = get_tool(step.get("tool_code", ""))
        out.append({
            "step": step.get("step"),
            "tool_code": step.get("tool_code"),
            "tool": tool,  # name/icon_emoji/section/time_minutes
            "step_name": step.get("step_name", ""),
            "why": step.get("why", ""),
            "time_min": step.get("time_min", 5),
        })
    return out


