def archetype_catalog_for_prompt() -> str:
    """Краткий каталог архетипов для системного промпта DeepSeek-экстрактора:
    DeepSeek выбирает один code из этого списка."""
    lines: List[str] = ["Каталог архетипов (по Марк–Пирсон). Выбери ОДИН code, который точнее всего описывает человека:"]
    for code, meta in ARCHETYPES.items():
        lines.append(
            f"• {code} ({meta['name_ru']}) — {meta['essence']} "
            f"Боль: {meta['target_pain']}. Цель: {meta['desired_outcome']}."
        )
    lines.append(
        "Если ни один не подходит уверенно — ставь null. Не выдумывай новые коды."
    )
    return "\n".join(lines)


def archetype_directives_for_outreach(code: Optional[str]) -> str:
    """Блок директив для copywriter-промпта vk_outreach.
    Когда архетип определён — дай DeepSeek конкретный тон и крючок."""
    if not code:
        return ""
    meta = ARCHETYPES.get(code)
    if not meta:
        return ""
    return (
        "АРХЕТИП ПОЛУЧАТЕЛЯ — учитывай при написании сообщения:\n"
        f"  • Архетип: {code} ({meta['name_ru']})\n"
        f"  • Суть: {meta['essence']}\n"
        f"  • Куда бьём (боль): {meta['target_pain']}\n"
        f"  • Что обещаем (не словом, а тоном): {meta['marketing_hook']}\n"
        f"  • Тон голоса: {meta['tone_of_voice']}\n"
        "Не цитируй эти подсказки в самом сообщении. Они нужны, чтобы выбрать ПРАВИЛЬНУЮ "
        "регистровую тональность и не написать Бунтарю как Невинному.\n"
    )


