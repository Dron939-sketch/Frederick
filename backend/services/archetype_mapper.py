# 3. ЭВРИСТИЧЕСКИЙ ФОЛБЭК ПО ВЕКТОРАМ
# ============================================================

def _vector_score(value: Optional[int], target: str) -> float:
    """Совпадение значения вектора с признаком архетипа («low»/«mid»/«high»)."""
    if value is None:
        return 0.0
    target = target.lower()
    if target == "low":
        return 1.0 if value <= 2 else 0.5 if value == 3 else 0.0
    if target == "low-mid":
        return 1.0 if value in (2, 3) else 0.5 if value in (1, 4) else 0.0
    if target == "mid":
        return 1.0 if value == 3 else 0.5 if value in (2, 4) else 0.0
    if target == "mid-high":
        return 1.0 if value in (4, 5) else 0.5 if value in (3, 6) else 0.0
    if target == "high":
        return 1.0 if value >= 5 else 0.5 if value == 4 else 0.0
    return 0.0


def infer_archetype_from_vectors(
    sb: Optional[int] = None,
    tf: Optional[int] = None,
    ub: Optional[int] = None,
    cv: Optional[int] = None,
) -> Optional[str]:
    """Подбираем код архетипа по векторам.
    Возвращает None, если все векторы пусты."""
    if all(v is None for v in (sb, tf, ub, cv)):
        return None

    best_code: Optional[str] = None
    best_score = -1.0
    for code, meta in ARCHETYPES.items():
        sig = meta["vector_signature"]
        score = (
            _vector_score(sb, sig.get("SB", "mid"))
            + _vector_score(tf, sig.get("TF", "mid"))
            + _vector_score(ub, sig.get("UB", "mid"))
            + _vector_score(cv, sig.get("CV", "mid"))
        )
        if score > best_score:
            best_score = score
            best_code = code
    return best_code


def get_archetype(code: Optional[str]) -> Optional[Dict[str, Any]]:
    """Достать meta архетипа по коду."""
    if not code:
        return None
    return ARCHETYPES.get(code.upper())


def all_codes() -> List[str]:
    return list(ARCHETYPES.keys())
