// ===== ВЫБОР НАВЫКА =====
function _scRenderSelect() {
    // Получаем результат диагностики если есть
    let diagBlock = '';
    try {
        const sdRaw = localStorage.getItem('sd_result_' + _scUid());
        if (sdRaw) {
            const sd = JSON.parse(sdRaw);
            const { SD_QUESTIONS } = (window._sdState ? {} : {});
            // Импортируем из skill_diagnosis если доступно
            const qs = window.SD_QUESTIONS_REF || null;
            // Строим список слабых навыков из диагностики
            const entries = Object.entries(sd.scores||{});
            if (entries.length > 0) {
                // Определяем доступные навыки из SC_EXERCISES
                const sphere = sd.sphere || 'personal';
                // Маппинг skill_id -> exercise id
                const SKILL_MAP = {
                    personal: ['confidence','discipline','boundaries','emotions','communication','resilience','focus','growth'],
                    professional: ['planning','decision','delegation','leadership','timemanage','feedback','networking','creativity']
                };
                const skillIds = SKILL_MAP[sphere] || [];
                // Сортируем по возрастанию баллов (слабые первые)
                const sorted = entries
                    .map(([qi, score]) => ({ qi: parseInt(qi), score }))
                    .sort((a,b) => a.score - b.score)
                    .slice(0, 3);

                const weakCards = sorted.map(({qi, score}) => {
                    const skillId = skillIds[qi];
                    const ex = SC_EXERCISES[skillId];
                    if (!ex) return '';
                    const pct = Math.round(score/4*100);
                    return `
                    <div class="sc-skill-card${_sc.skillId===skillId?' active':''}" data-id="${skillId}">
                        <div class="sc-skill-body">
                            <div class="sc-skill-name">${ex.name}</div>
                            <div class="sc-skill-desc">${ex.desc}</div>
                            <div class="sc-skill-bar-wrap">
                                <div class="sc-skill-bar-fill" style="width:${pct}%"></div>
                            </div>
                        </div>
                        <div class="sc-skill-score">${score}/4</div>
                    </div>`;
                }).join('');

                if (weakCards.trim()) {
                    diagBlock = `
                        <div class="sc-section-label">📊 Из вашей диагностики — топ-3 слабых</div>
                        ${weakCards}`;
                }
            }
        }
    } catch {}

    // Все доступные навыки
    const allSkills = Object.entries(SC_EXERCISES).map(([id, ex]) => `
        <div class="sc-skill-card${_sc.skillId===id?' active':''}" data-id="${id}">
            <div class="sc-skill-body">
                <div class="sc-skill-name">${ex.name}</div>
                <div class="sc-skill-desc">${ex.desc}</div>
            </div>
        </div>`).join('');

    // Активный план
    const activePlan = _sc.skillId ? `
        <div style="background:rgba(224,224,224,0.06);border:1px solid rgba(224,224,224,0.18);border-radius:14px;padding:12px 14px;margin-bottom:16px;display:flex;justify-content:space-between;align-items:center">
            <div>
                <div style="font-size:13px;font-weight:600;color:var(--chrome)">${_sc.skillName || ''}</div>
                <div style="font-size:11px;color:var(--text-secondary)">День ${_scCurrentDay()} из 21 · выполнено ${_sc.daysDone.length}</div>
            </div>
            <button class="sc-btn sc-btn-primary" id="scOpenPlan" style="width:auto;padding:9px 16px;border-radius:30px">Открыть →</button>
        </div>` : '';

    return `
        ${activePlan}
        ${diagBlock}
        <div class="sc-section-label" style="margin-top:${diagBlock?'18px':'0'}">📚 Все навыки</div>
        <div style="margin-bottom:16px">${allSkills}</div>

        <div class="sc-custom-block">
            <div class="sc-custom-label">✏️ Или введите свой навык:</div>
            <input class="sc-input" id="scCustomInput" placeholder="Например: публичные выступления, скорочтение...">
        </div>

        <button class="sc-btn sc-btn-primary" id="scStartBtn" style="margin-top:14px">
            🎯 Начать 21-дневный план
        </button>`;
}

// ===== ПЛАН =====
function _scRenderPlan() {
    const ex   = _scGetExercise(_sc.skillId);
    const day  = _scCurrentDay();
    const done = _sc.daysDone;

    // Если навык введён вручную — показываем общий 7-дневный шаблон
    const isCustom = !ex;
    const skillName = _sc.skillName || 'Навык';

    // Три недели
    const weeks = ex ? ex.weeks : [{
        theme: 'Неделя 1', exercises: Array.from({length:7},(_,i)=>({ task:`День ${i+1}: практика навыка`, dur:'15 мин', inst:`Посвятите 15 минут целенаправленной практике: ${skillName}. Запишите что получилось.` }))
    }, {
        theme: 'Неделя 2', exercises: Array.from({length:7},(_,i)=>({ task:`День ${i+8}: углубление`, dur:'20 мин', inst:`Усложните практику: ${skillName}. Попробуйте что-то новое в этом навыке.` }))
    }, {
        theme: 'Неделя 3', exercises: Array.from({length:7},(_,i)=>({ task:`День ${i+15}: закрепление`, dur:'15 мин', inst:`Закрепите ${skillName} в реальной ситуации. Отметьте изменения.` }))
    }];

    // Календарь 3×7
    const calWeeks = weeks.map((week, wi) => {
        const days = week.exercises.map((e, di) => {
            const dayNum = wi * 7 + di + 1;
            const isDone    = done.includes(dayNum);
            const isCurrent = dayNum === day;
            return `<div class="sc-day${isDone?' done':''}${isCurrent&&!isDone?' current':''}" data-day="${dayNum}">
                <div class="sc-day-num">${dayNum}</div>
                <div class="sc-day-dot"></div>
            </div>`;
        }).join('');
        return `
        <div class="sc-week">
            <div class="sc-week-label">Неделя ${wi+1} · ${week.theme}</div>
            <div class="sc-week-grid">${days}</div>
        </div>`;
    }).join('');

    // Текущая задача
    const curWi = Math.floor((day-1)/7);
    const curDi = (day-1) % 7;
    const curEx = weeks[curWi]?.exercises[curDi];
    const isDoneToday = done.includes(day);

    const currentBlock = curEx ? `
        <div class="sc-section-label" style="margin-top:20px">⚡ День ${day} — сегодня</div>
        <div class="sc-exercise-card current">
            <div class="sc-exercise-header">
                <div class="sc-exercise-num">${day}</div>
                <div class="sc-exercise-task">${curEx.task}</div>
            </div>
            <div class="sc-exercise-dur">⏱ ${curEx.dur}</div>
            <div class="sc-exercise-inst">${curEx.inst}</div>
            ${!isDoneToday
                ? `<button class="sc-btn sc-btn-primary" id="scMarkDone" style="margin-top:12px">✅ Отметить выполнение</button>`
                : `<div style="margin-top:10px;font-size:12px;color:var(--text-secondary)">✅ Выполнено сегодня</div>`
            }
        </div>` : '';

    // Прогресс
    const pct = Math.round((done.length / 21) * 100);

    return `
        <div class="sc-plan-header">
            <div class="sc-plan-skill">${skillName}</div>
            <div class="sc-plan-meta">
                День ${day} из 21 · выполнено ${done.length} · прогресс ${pct}%
            </div>
            <div class="sc-skill-bar-wrap" style="margin-top:10px;height:4px">
                <div class="sc-skill-bar-fill" style="width:${pct}%"></div>
            </div>
        </div>

        ${calWeeks}
        ${currentBlock}

        <div class="sc-btn-row" style="margin-top:20px">
            <button class="sc-btn sc-btn-ghost" id="scResetPlan">🔄 Начать заново</button>
            <button class="sc-btn sc-btn-ghost" id="scGoTraining">⚡ К тренировке</button>
        </div>`;
}

