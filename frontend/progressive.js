  async function submit(n) {
    if (ST.busy) return;
    var D = DAYS[n - 1]; if (!D) return;
    var inp = document.getElementById('pgNote'); if (!inp) return;
    var note = inp.value.trim();
    if (note.length < 30) { toast('Опиши подробнее — хотя бы 2–3 предложения', 'info'); return; }
    ST.busy = true;
    var out = document.getElementById('pgOut');
    if (out) out.innerHTML = '<div class="pg-typing">📚 Фреди-наставник читает твой отчёт…</div>';
    var prompt = 'Ты — Фреди-наставник в игре «Вариатика. Уровень Progressive» (по А. Мейстеру, переход в сектор ЧВ). Игрок сдаёт уровень ' + n + ' из 7: «' + D.title + '». Стержень уровня: «' + D.core + '».\n' +
      'Задание уровня было: ' + D.task.replace(/<[^>]+>/g, '') + '\n\n' +
      'ОТЧЁТ ИГРОКА:\n"""\n' + note + '\n"""\n\n' +
      'Дай разбор: ' + D.check + ' Тон: на «ты», по делу, тёплый, без воды и без оценок «молодец/плохо». 4–6 предложений связной речью, без нумерации. Не пересказывай задание. Без служебных тегов.';
    var r = await aiGenerate(prompt, { temperature: 0.65, max_tokens: 380 });
    var fb = (r && r.success && r.content) ? clean(r.content) : 'Ты сделал шаг — это уже движение. Если что-то осталось непонятным, перечитай теорию уровня и попробуй на одном конкретном человеке. Готов идти дальше — продолжай.';
    var p = loadProg();
    var wasDone = !!p.done[n];
    p.notes[n] = note; p.notes[n + '_fb'] = fb; p.done[n] = true; if (n + 1 > p.day) p.day = n + 1; saveProg(p);
    if (!wasDone) seriesAdd('progressive', 5); // +5 очков серии за каждый новый сданный уровень
    if (out) out.innerHTML = '<div class="pg-fb">' + nl2br(fb) + '</div>' +
      '<div class="pg-row">' +
        (n < 7 ? '<button class="pg-btn pg-primary" style="flex:1;width:auto;margin:0;padding:13px" onclick="PROGRESSIVE.day(' + (n + 1) + ')">▶ Перейти на уровень ' + (n + 1) + '</button>'
              : '<button class="pg-btn pg-primary" style="flex:1;width:auto;margin:0;padding:13px" onclick="PROGRESSIVE.home()">🏆 К финалу игры</button>') +
        '<button class="pg-btn" style="width:auto;margin:0;padding:13px" onclick="PROGRESSIVE.home()">К уровням</button>' +
      '</div>';
    ST.busy = false;
    track('feature_opened', { feature: 'progressive_day_done', day: n });
  }

